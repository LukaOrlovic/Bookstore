    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        k.setNazivKnjige(txtNaziv.getText());
        k.setZanr(txtZanr.getText());
        
        if(Double.parseDouble(txtCena.getText().trim()) <= 0){
            JOptionPane.showMessageDialog(this, "Cena ne moze biti manja ili jednaka 0.");
            return;
        }
        
        k.setCena(Double.parseDouble(txtCena.getText().trim()));
        k.setAutor((Autor) cmbAutor.getSelectedItem());
        k.setPoreskaStopa((PoreskaStopa) cmbStopa.getSelectedItem());
        
        ZahtevKlijenta zh = new ZahtevKlijenta();
        zh.setOperacija(ZahtevKlijenta.IZMENI_KNJIGU);
        zh.setObjekatOperacije(k);

        try {

            OdgovorServera os = (OdgovorServera) Komunikacija.vratiInstancu().requestSynch(zh);
            if (os.getSignal() == -1) {
                os.getIzuzetak();
                JOptionPane.showMessageDialog(this, "Sistem ne moze da izmeni knjigu.", "Izmena knjige.", JOptionPane.INFORMATION_MESSAGE);

            }

            JOptionPane.showMessageDialog(this, "Uspesno ste izmenili knjigu", "Izmena knjige", JOptionPane.INFORMATION_MESSAGE);

            this.dispose();

            //aktivnost= new Aktivnost(polaznikKursa, -1, 0, 0, 0, tema, profesor);
        } catch (Exception ex) {
            Logger.getLogger(PocetnaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

