    public void popuni() {

        ArrayList<Autor> autori = KontrolerForma.getInstance().popuniListuAutora();
        cmbAutor.removeAllItems();
        for (Autor a : autori) {
            cmbAutor.addItem(a);
            if(k.getAutor().proveri(a)){
                cmbAutor.setSelectedItem(a);
            }
        }
        
        ArrayList<PoreskaStopa> stope = KontrolerForma.getInstance().popuniListuStopa();
        cmbStopa.removeAllItems();
        for (PoreskaStopa ps : stope) {
            cmbStopa.addItem(ps);
            if(k.getPoreskaStopa().proveri(ps)){
                cmbStopa.setSelectedItem(ps);
            }
        }
        
        
        txtNaziv.setText(k.getNazivKnjige());
        txtZanr.setText(k.getZanr());
        txtCena.setText(k.getCena()+"");
        
   }
